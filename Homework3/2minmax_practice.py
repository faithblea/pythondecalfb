list1 = [(-1, 3), (4, 7), (8, 1), (3, 5)]
 
# using min() and max()functions find the min and max of functions
# to get min and max in list of tuples
pro1 = min(list1), max(list1)
 
# printing result in the string form of pro1
print ("The min and max of list 1 : " + str(pro1))

